
function somar(a: number, b: number ): number {
    return a + b
}

console.log(somar(10, 2))

function subtrair(a: number, b: number ): number {
    return a - b
}

console.log(subtrair(10, 2))

function divisao(a: number, b: number ): number {
    return a / b
}


console.log(divisao(10, 2))

